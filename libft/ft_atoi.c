#include "libft.h"

long	ft_atoi(const char *str)
{
	long	sign;
	long	res;
	long	new_res;
	int		i;

	i = 0;
	while ((str[i] >= '\t' && str[i] <= '\r') || str[i] == ' ')
		i++;
	if (str[i] == '-')
		sign = -1;
	else
		sign = 1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	res = 0;
	while (str[i] >= '0' && str[i] <= '9')
	{
		new_res = (res * 10) + (str[i] - '0');
		if (new_res < res)
			return (-1);
		res = new_res;
		i++;
	}
	return (res * sign);
}
