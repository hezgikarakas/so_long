#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*last_elm;

	if (!*lst)
	{
		*lst = new;
		return ;
	}
	last_elm = ft_lstlast(*lst);
	last_elm->next = new;
}
